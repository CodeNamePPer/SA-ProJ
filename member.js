function openCart(){
    $('#modalCart').css('display','flex')
    rendercart();
}

function rendercart(){
    if(cart.length > 0){
        var html = '';
        let totalPrice = 0;
        for (let i = 0; i < cart.length; i++) {
            const itemTotal = cart[i].price * cart[i].count;
            totalPrice += itemTotal; 
            html += `<div class="cartlist-items">
                   <div class="cartlist-left">
                   <img class="modaldesc-img" src="./IphoneImg/${cart[i].img}" alt="">
                    <div class="cartlist-detail">
                        <p style="font-size: 1.2vw;">${cart[i].name}</p>
                        <p style="font-size: 1vw;">${numberWithCommas(cart[i].price * cart[i].count)} $</p>
                    </div>

                   </div>

                   <div class="cartlist-right">
                        <p onclick="deinitems('-',${i})"  class="btnc">-</p>
                        <p id="countitems${i}"style="margin: 0 20px;">${cart[i].count}</p>
                        <p onclick="deinitems('+',${i})"  class="btnc">+</p>
                   </div>

                 </div>`;
            
        }
            html +=`<div class="sum-price">Total:${ numberWithCommas(totalPrice) } $</div>
                    
            `;
        $("#mycart").html(html)
    }
    else{
        $("#mycart").html(`<p>Not found product list</p>`)
    }
}

function deinitems(action, index){
    if(action == '-'){
        if(cart[index].count > 0){
            cart[index].count--;
            $("#countitems"+index).text(cart[index].count)
            if(cart[index].count <=0){
                Swal.fire({
                    icon: 'warning',
                    title: 'Are you sure to Delete',
                    showConfirmButton: true,
                    showCancelButton: true,
                    confirmButtonText: 'Delete',
                    cancelButtonText: 'Cancel'

                }).then((res) => {
                    if(res.isConfirmed){
                        cart.splice(index, 1)
                        rendercart();
                        $("#cartcount").css('display','flex').text(cart.length)

                        if(cart.length <=0){
                            $("#cartcount").css('display', 'none')
                        }  
                    }
                    else{
                        cart[index].count++;
                        $("#countitems"+index).text(cart[index].count)
                        rendercart();
                    }
                })
            }
            rendercart();
        }
    }else if(action == '+'){
        cart[index].count++;
        $("#countitems"+index).text(cart[index].count)
        rendercart();
    }
}

